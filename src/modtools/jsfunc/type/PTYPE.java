package modtools.jsfunc.type;

/** 原始数据类型和基本的类
 * @see java.lang.Class#getName */
public interface PTYPE {
	Class<?> Z = boolean.class;
	Class<?> B = byte.class;
	Class<?> C = char.class;
	Class<?> D = double.class;
	Class<?> F = float.class;
	Class<?> I = int.class;
	Class<?> J = long.class;
	Class<?> S = short.class;

	Class<?> V = void.class;

	Class<?> L = Object.class;
	Class<?> STR = String.class;
}
